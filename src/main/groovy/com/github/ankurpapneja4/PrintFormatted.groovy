package com.github.ankurpapneja4;

// Integers
def n = 109985000

printf "%d", n  // 109985000
printf "%n"     // New Line Character
printf "%,d",n  // 109,985,000


// Binary
def b = 0xA4

printf "%d %n",  b // 164
printf "%h %n",  b // a4
printf "%H %n",  b // A4
printf "%x %n",  b // a4
printf "%X %n",  b // A4
printf "%#X %n", b // 0xA4

